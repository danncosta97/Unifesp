package Ex2;

public abstract class GeradorClasse {
	private String nome;
	private String extende;
	
	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getExtende() {
		return extende;
	}

	public void setExtende(String extende) {
		this.extende = extende;
	}

	public String getNome() {
		return nome;
	}
	
	
}
