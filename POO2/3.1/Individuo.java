
public class Individuo {
	private int num;
	
	public Individuo() {
		super();
		this.num = num;
	}
	
	public Individuo(int num) {
		super();
		this.num = num;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public int getAssento() {
		return getNum();
	}
}
