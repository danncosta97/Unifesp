package Hometheater;

public class Som {
	public int volume;
	public int amplificador;
	
	public Som() {
		volume = 0;
		amplificador = 0;
	}
	
	public int getVolume() {
		return volume;
	}
	public void setVolume(int volume) {
		this.volume = volume;
	}
	public int getAmplificador() {
		return amplificador;
	}
	public void setAmplificador(int amplificador) {
		this.amplificador = amplificador;
	}

}
